function onDocumentMouseDown( event ) {
    event.preventDefault();
    // Get the mouse position in normalized device coordinates
    mouse.x = ( event.clientX / renderer.domElement.clientWidth ) * 2 - 1;
    mouse.y = - ( event.clientY / renderer.domElement.clientHeight ) * 2 + 1;
    // Update the raycaster with the mouse position
    raycaster.setFromCamera( mouse, camera );
    // Get an array of objects that the raycaster intersects
    var intersects = raycaster.intersectObjects(scene.children);
    if ( intersects.length > 0 ) {
        // If an object is clicked, log its name to the console
        console.log( intersects[ 0 ].object.name + " clicked!" );
    }
}

